# Relative tolerance for lower bound testing
rel_tol = 0.01

def _dp_sgd_poisson_rdp(q, sigma, steps, order):
    sample = AmplificationBySampling(PoissonSampling=True)
    compose = Composition()
    gaussian_mechanism = ExactGaussianMechanism(sigma)
    dp_sgd = compose([sample(gaussian_mechanism, q)], [steps])
    return dp_sgd.get_RDP(order)


class TestDPSGDPoisson(parameterized.TestCase):

    @parameterized.parameters(p for p in params)
    def test_RDP_values_lowerbound(self, q, sigma, steps, order, tfp_rdp_value):
        autodp_rdp_value = _dp_sgd_poisson_rdp(q, sigma, steps, order)
        self.assertGreaterEqual(autodp_rdp_value, (1. - rel_tol) * tfp_rdp_value)


